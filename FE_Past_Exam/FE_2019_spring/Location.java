package FE_2019_spring;

/**
 * 現在座標クラス
 */
public class Location
{
    /**
     * The X.
     */
    public final int x;
    /**
     * The Y.
     */
    public final int y;

    /**
     * コンストラクタ
     *
     * @param x the x
     * @param y the y
     */
    public Location(int x, int y)
    {
        this.x = x;
        this.y = y;
    }
}